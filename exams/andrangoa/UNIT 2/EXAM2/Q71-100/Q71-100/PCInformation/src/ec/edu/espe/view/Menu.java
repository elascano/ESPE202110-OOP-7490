    private void read() {
MongoCursor<org.bson.Document> consulta = InformationPc.find().iterator();
        
        int total = tabla.getRowCount();
        for(int i = 0; i<total; i++){
            tabla.removeRow(0);
        }
        while(consulta.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(consulta.next().values());
            tabla.addRow(doc.toArray());
    }


}

    public boolean Delete(String id){
        DeleteResult answer = InformationPc.deleteOne(new org.bson.Document("_id", new ObjectId(id)));
        if(answer.getDeletedCount()==1){
            return true;
        }
        return false;
    }
