    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int renglon = tblInformation.getSelectedRow();
        if(renglon == -1){
            JOptionPane.showMessageDialog(this, "Error, Select the computer to delete");
            return;
        }
        String SerialNumberRemove = tblInformation.getValueAt(renglon, 0).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, "Are you sure to delete the Serial Number"+ SerialNumberRemove);
        if(respuesta == JOptionPane.OK_OPTION){
            boolean answerDelete = Delete(SerialNumberRemove);
            if(answerDelete==true){
                read();
                JOptionPane.showMessageDialog(this, "correct delety");
            }else{
                JOptionPane.showMessageDialog(this, "delety error");

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

