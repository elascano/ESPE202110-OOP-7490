    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
         ArrayList<String> arrStr = new ArrayList<String>();
        Controller arrayController = new Controller();
        arrStr = arrayController.JoinArray(arrStr, value);
        ArrayList<Integer> arrInt = new ArrayList<>();
        arrInt = arrayController.CreateArray(arrStr);
        int data[] = arrayController.ChangeVector(arrInt);
        SortingContext sc = new SortingContext();
        int sortedList[] = sc.sort(data);
        JOptionPane.showMessageDialog(null, "Su vector ordenado es: ");
        String algorithm = "";
        if (value >= 0 && value <= 3) {
            algorithm = "BubbleSort";
        } else if (value >= 4 &&value <= 7) {
            algorithm = "InsertionSort";
        } else if (value > 7) {
            algorithm = "QuickSort";
        }
        ArrayList<Integer> datas = new ArrayList<Integer>();

        Array array = new Array(arrStr,value, datas, algorithm);
        arrayList.add(array);

        for (int k = 0; k < data.length; k++) {
            datas.add(data[k]);
        }
        JOptionPane.showMessageDialog(null, datas + " Vector ordenado ");
        mostrar();
        Gson gson = new Gson();
        String arrJson;
        arrJson = gson.toJson(array);
        FileManager.save("VectorList.json", arrJson);
    }//GEN-LAST:event_jButton1ActionPerformed

