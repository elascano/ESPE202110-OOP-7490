    public ArrayList<Register> returnPerson(){
        
        ArrayList<Register> person;
        person = new ArrayList<>();
        MongoCursor<Document> consulta = Registers.find().iterator();
        
        while(consulta.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(consulta.next().values());
            
            int idNumber = Integer.parseInt(doc.get(1).toString());
            String lastName = doc.get(2).toString();
            String name = doc.get(3).toString();
            //Date birthdate = Integer.valueOf(doc.get(4).toString());
            float salary = Float.parseFloat(doc.get(5).toString());  
            
            Register personData = new Register(idNumber, lastName, name, birthdate, salary);        
            
            person.add(personData);
        }
        return person;
    }
    
    public Register findPerson(int idNumber){
        
        ArrayList<Register> person;
        person = new ArrayList<>();
        person = returnPerson();
        
        for(Register personData:person){
            if(personData.getIdNumber()== idNumber){
                return personData;
            }
        }
        return null; 
    }
