    private void txtIdKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdKeyTyped
        int key = evt.getKeyChar();
        boolean numbers = (key >= 48 && key <= 57);
        boolean supr = (key == 8);

        if (numbers || supr) {
            lblErrorId.setForeground(new Color(102,255,255));
            lblErrorId.setText("Correct Format");
        }else{
            JOptionPane.showMessageDialog(this, "Fill only with numbers from 0 to 9");
            lblErrorId.setForeground(new Color(255,0,0));
            lblErrorId.setText("Error. Enter only numbers");
            evt.consume();
        }
        habilyInsert();
        habilyClean();
    }//GEN-LAST:event_txtIdKeyTyped

    private void txtLastNameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtLastNameKeyTyped
        int key = evt.getKeyChar();
        boolean uper = (key >= 65 && key <= 90);
        boolean lower = (key >= 97 && key <= 122);
        boolean espace = (key == 32);
        boolean supr = (key == 8);

        if ((uper || lower || espace || supr)) {
            lblErrorLastN.setForeground(new Color(102,255,255));
            lblErrorLastN.setText("Correct Format");
        }else{
            JOptionPane.showMessageDialog(this, "Fill only with uppercase and lowercase letters");
            lblErrorLastN.setForeground(new Color(255,0,0));
            lblErrorLastN.setText("Error. Enter only letters");
            evt.consume();
        }
        habilyInsert();
        habilyClean();
    }//GEN-LAST:event_txtLastNameKeyTyped

