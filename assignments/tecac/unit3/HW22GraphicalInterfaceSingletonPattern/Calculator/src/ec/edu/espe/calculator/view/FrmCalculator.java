    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        DecimalFormat decimal = new DecimalFormat("#.00");
        
        String inputValue = txtValue.getText();
        String inputPercentage = spiPercentage.getValue().toString();
        double value = Double.parseDouble(inputValue);
        double percentage= Double.parseDouble(inputPercentage);
        double totalPercentage = (value*percentage)/100;
        double total = value + totalPercentage;
        
        txtPercentage.setText(String.valueOf(decimal.format(totalPercentage)));
        txtTotal.setText(String.valueOf(decimal.format(total)));
        JOptionPane.showMessageDialog(null,"The entered value was --> " + value +" $"+"\nThe entered percentage was --> "+ percentage
                +" %"+"\n The calculated percentage is  --> " + decimal.format(totalPercentage) +" %"+"\n The total to pay is -->" + decimal.format(total)+" $");
    }//GEN-LAST:event_btnCalculateActionPerformed

