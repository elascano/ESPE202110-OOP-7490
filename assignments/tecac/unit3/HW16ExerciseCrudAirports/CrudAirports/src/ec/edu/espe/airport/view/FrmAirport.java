    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        txtName.getText();
        cmbOrigin.getSelectedItem();
        cmbDestination.getSelectedItem();
        txtIdAirline.getText();
        txtAirline.getText();
        txtTicketValue.getText();

        Object airport[] = new Object[7];
        airport[1] = txtName.getText();
        airport[2] = cmbOrigin.getSelectedItem();
        airport[3] = cmbDestination.getSelectedItem();
        airport[4] = txtIdAirline.getText();
        airport[5] = txtAirline.getText();
        airport[6] = txtTicketValue.getText();
        tableAirports.addRow(airport);

        try {
            Document data = new org.bson.Document();
            data.put("Name", txtName.getText());
            data.put("Origin", cmbOrigin.getSelectedItem());
            data.put("Destination", cmbDestination.getSelectedItem());
            data.put("Id Airline", txtIdAirline.getText());
            data.put("Airline", txtAirline.getText());
            data.put("Ticket Value", txtTicketValue.getText());
            Airports.insertOne(data);
            JOptionPane.showMessageDialog(this, "Data Added Successful ");
            txtName.setText("");
            cmbOrigin.setSelectedItem("");
            cmbDestination.setSelectedItem("");
            txtIdAirline.setText("");
            txtAirline.setText("");
            txtTicketValue.setText("");

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this, "ERROR: " + err.getMessage());
        }
    }//GEN-LAST:event_btnInsertActionPerformed

