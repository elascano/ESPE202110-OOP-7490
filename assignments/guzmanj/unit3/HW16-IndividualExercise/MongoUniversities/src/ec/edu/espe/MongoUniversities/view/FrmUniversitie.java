    private void bttUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttUploadActionPerformed
        int renglon=tb1Universities.getSelectedRow();
            if(renglon==-1){
                JOptionPane.showMessageDialog(this, "Error, Select the row to update");
                return;
            }
            String idUpload=tb1Universities.getValueAt(renglon, 0).toString();
            int answer = JOptionPane.showConfirmDialog(this, "Are you sure to update the ID?" + idUpload);
            if(answer==JOptionPane.OK_OPTION){
                String id=txtId.getText();txtId.setText("");
                String name=txtName.getText();txtName.setText("");
                String direction=txtDirection.getText();txtDirection.setText("");
                String phone=txtPhone.getText();txtPhone.setText("");
        
                String[]status=new String[1];
                bttStatus.add(bttPublic);
                bttStatus.add(bttPrivate);
                if(bttPublic.isSelected()){status[0]="Public";}
                else if(bttPrivate.isSelected()){status[0]="Private";}
        
                DBManager.update(idUpload, id, name, status[0], direction, phone);
                DBManager.read(table);
            }
            
    }//GEN-LAST:event_bttUploadActionPerformed
    
