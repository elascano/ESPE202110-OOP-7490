     private ArrayList<User> returnUsers() {
         ArrayList<User> users;
        users = new ArrayList<>();
        MongoCursor<Document> consulta = User.find().iterator();
        
        while(consulta.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(consulta.next().values());
            
            int id = Integer.parseInt(doc.get(1).toString());
            String lastName = doc.get(2).toString();
            String name =doc.get(3).toString();
            String birthDate = doc.get(4).toString();
            float salary = Float.parseFloat(doc.get(5).toString());
            
           
            
            User user = new User(id, lastName, name, birthDate, salary);
            users.add(user);
        }
        return users;
    }


    private User findUsers(int id) {
        ArrayList<User> users;
        users = new ArrayList<>();
        users = returnUsers();
        
        for(User user:users){
            if(user.getId()==id){
                return user;
            }
        }
        return null; 
    }

   
    
    private boolean Delete(String id) {
        DeleteResult answer = User.deleteOne(new org.bson.Document("_id", new ObjectId(id)));
        return answer.getDeletedCount()==1;
    }
