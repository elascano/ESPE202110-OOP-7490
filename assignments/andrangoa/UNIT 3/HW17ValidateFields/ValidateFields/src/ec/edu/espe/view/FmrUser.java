    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        int option;
        String id = txtId.getText();
        option=JOptionPane.showConfirmDialog(this, "Are you sure you want to register " + id);
        if(option == 0){
            JOptionPane.showMessageDialog(this, "Registering" + id);
        try{
        org.bson.Document data = new org.bson.Document();
        
        data.put("Id", txtId.getText());
        data.put("Last Name", txtLastName.getText());
        data.put("Name", txtName.getText());
        data.put("Birth Date", txtBirthDate.getText());
        data.put("Salary", txtSalary.getText());
        
        
        User.insertOne(data);
        JOptionPane.showMessageDialog(this, "Succesful");
        } catch(HeadlessException err){
            
        }
            
        } else if(option==JOptionPane.NO_OPTION){
            JOptionPane.showMessageDialog(this, id + "was not registered");
        }
        txtId.setText("");
        txtLastName.setText("");
        txtName.setText("");
        txtBirthDate.setText("");
        txtSalary.setText("");
    }//GEN-LAST:event_btnRegisterActionPerformed

