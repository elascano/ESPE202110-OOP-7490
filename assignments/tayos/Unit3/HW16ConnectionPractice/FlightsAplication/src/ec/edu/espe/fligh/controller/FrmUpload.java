    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        Document dataUpload = new Document();

        dataUpload.put("id", Integer.parseInt(txtId.getText()));
        dataUpload.put("Place of Departure", txtDeparture.getText());
        dataUpload.put("Destiny", txtDestiny.getText());
        dataUpload.put("FlightDate", dateOfFlightChooser.getDate().toString());
        dataUpload.put("Flight Class", ComboClasses.getSelectedItem().toString());

        UpdateResult answer = Flights.updateOne(filter, new Document("$set", dataUpload));
        if (answer.getMatchedCount() == 1) {
            JOptionPane.showMessageDialog(this, "Success got Updated");
        } else {
            JOptionPane.showMessageDialog(this, "Error, not Update");
        }

    }//GEN-LAST:event_btnUploadActionPerformed

