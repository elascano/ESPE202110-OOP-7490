    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String education = null;
        if(rbtnPublic.isSelected()){
                education = "Public";
            }else if (rbtnPrivate.isSelected()){
                education = "Private";
            }
        try {
            Document data = new Document();
            
            data.put("Name", txtName.getText());
            data.put("Public/Private", education);
            data.put("Amount of Students", Float.valueOf(txtStudents.getText()));
            data.put("Amount of Teachers", Float.valueOf(txtTeachers.getText()));
            data.put("Class Schedeule", lstSchedule.getSelectedValuesList().toString());
            HighSchool.insertOne(data);
            
            JOptionPane.showMessageDialog(this, "**High School Added**");
           
        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "ERROR!: "+err.getMessage());
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListActionPerformed
        MongoCursor<Document> contactQuery = HighSchool.find().iterator();
       
        while(contactQuery.hasNext()){
            ArrayList<Object> contactDoc = new ArrayList<Object>(contactQuery.next().values());
            table.addRow(contactDoc.toArray());
        }
    }//GEN-LAST:event_btnListActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        MongoCursor<Document> query = HighSchool.find().iterator();
        
        int total = table.getRowCount();
        for(int i = 0; i<total; i++){
            table.removeRow(0);
        }
        while(query.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(query.next().values());
            table.addRow(doc.toArray());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblHighSchool.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Error, select the row to delete");
            return;
        }
        String idRemove = tblHighSchool.getValueAt(row, 0).toString();
        int answer = JOptionPane.showConfirmDialog(this, "Are you sure to delete the id?  "+ idRemove);
        if(answer == JOptionPane.OK_OPTION){
            boolean answerDelete = Delete(idRemove);
            if(answerDelete==true){
                JOptionPane.showMessageDialog(this, "**Successfully Removed**");
            }else{
                JOptionPane.showMessageDialog(this, "Could not be removed");

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

