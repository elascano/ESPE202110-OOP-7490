    public void loadTable(){
        Vector<String> titlesTable=new Vector<String>();
        titlesTable.add("ID");
        titlesTable.add("Name");
        titlesTable.add("Cell Phone");
        titlesTable.add("Sex");
        titlesTable.add("Group");
        titlesTable.add("Salary");
        titlesTable.add("Hobby");
        titlesTable.add("Birth Data");
        titlesTable.add("Comments");
        
        DefaultTableModel modeloTabla=new DefaultTableModel (titlesTable,0);
        
        ContactsService servicio=new ContactsService();
        List<Contact> contactList=servicio.obtenerTodos();        

        for (Contact contact : contactList) 
        {
            Vector<String>filaTabla=new Vector<String>();
            
            
            filaTabla.add(contact.getName());
            filaTabla.add(contact.getCellPhoneNumber());
            filaTabla.add(contact.getHobby());
            filaTabla.add(contact.getGroup());
            filaTabla.add(contact.getSex());
            filaTabla.add(contact.getComments());
                        
            
            modeloTabla.addRow(filaTabla);
            
        }
        
        tblContacts.setModel(modeloTabla);
    }
