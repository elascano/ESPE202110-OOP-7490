    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        int renglon = panel.getSelectedRow();
        if(renglon == -1){
            JOptionPane.showMessageDialog(this, "Selecciona el ID a borrar");
            return;
        }
        String idRemove = panel.getValueAt(renglon, 0).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, "Â¿Estas seguro de eliminar este ID?"+ idRemove);
        if(respuesta == JOptionPane.OK_OPTION){
            boolean answerDelete = Delete(idRemove);
            if(answerDelete==true){
                mostrar();
                JOptionPane.showMessageDialog(this, "El proceso se realizo correctamente");
            }else{
                JOptionPane.showMessageDialog(this, "Error");

            }
        }
    }//GEN-LAST:event_removeActionPerformed

    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        try {
            Document data = new Document();
            
            data.put("Name", txtid.getText());
            data.put("Chassis", txtlast.getText());
            data.put("Capacity", txtname.getText());
            data.put("Type", txtsalary.getText());
            data.put("Owner", txtbirth.getText());
            Register.insertOne(data);
            
            JOptionPane.showMessageDialog(this, "CORRECT");
           
        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "ERROR: "+err.getMessage());
        }
    }//GEN-LAST:event_AddActionPerformed

