    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        Document dataUpload= new Document();
        
            dataUpload.put("IdName", Integer.parseInt(txtid.getText()));
            dataUpload.put("LastName", Integer.parseInt(txtid.getText()));
            dataUpload.put("Name", txtname.getText());
            dataUpload.put("Salary", Integer.parseInt(txtsalary.getText()));
            dataUpload.put("DateofBirth", Integer.parseInt(txtbirth.getText()));
        
        UpdateResult answer = exercise.updateOne(filter, new Document("$set",dataUpload));
        if(answer.getMatchedCount()==1){
            JOptionPane.showMessageDialog(this, "Success got Updated");
        } else {
            JOptionPane.showMessageDialog(this, "Error Could not Update");
        }
    }//GEN-LAST:event_btnUploadActionPerformed

