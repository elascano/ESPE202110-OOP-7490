    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int renglon = tblRegistration.getSelectedRow();
        if (renglon == -1) {
            JOptionPane.showMessageDialog(this, "Error, please select the row to delete");
            return;
        }
        String idRemove = tblRegistration.getValueAt(renglon, 0).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this id?" + idRemove);
        if (respuesta == JOptionPane.OK_OPTION) {
            boolean answerDelete = Delete(idRemove);
            if (answerDelete == true) {
                mostrar();
                JOptionPane.showMessageDialog(this, "Correct");
            } else {
                JOptionPane.showMessageDialog(this, "Error");

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

