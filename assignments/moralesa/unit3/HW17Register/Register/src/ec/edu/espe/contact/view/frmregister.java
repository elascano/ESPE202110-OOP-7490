        private void txtIdKeyTyped(java.awt.event.KeyEvent evt) {                               
        int key = evt.getKeyChar();
        boolean numbers = (key >= 48 && key <= 57);
        boolean supr = (key == 8);

        if (numbers || supr) {
            lblErrorId.setForeground(new Color(102,255,255));
            lblErrorId.setText("Correct Format");
        }else{
            JOptionPane.showMessageDialog(this, "Fill only with numbers from 0 to 9");
            lblErrorId.setForeground(new Color(255,0,0));
            lblErrorId.setText("Error. Enter only numbers");
            evt.consume();
        }
        habilyInsert();
        habilyClean();
    }
    
