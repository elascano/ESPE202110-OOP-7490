    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        String[] add = new String[5];

        add[0] = TxtId.getText();
        add[1] = TxtName.getText();
        add[2] = TxtPrice.getText();
        add[3] = (String) CmbGroup.getSelectedItem();
        add[4] = (String) CbmType.getSelectedItem();
        model.addRow(add);
    }//GEN-LAST:event_BtnAddActionPerformed

    private void BtnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUploadActionPerformed
        try {
            Document data = new org.bson.Document();
            data.put("Id", TxtId.getText());
            data.put("Name", TxtName.getText());
            data.put("Price", TxtPrice.getText());
            data.put((String) "Group", CmbGroup.getSelectedItem());
            data.put((String) "Type", CbmType.getSelectedItem());
            ToolList.insertOne(data);
            JOptionPane.showMessageDialog(this, "Successful");

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this, "ERROR: " + err.getMessage());
        }

    }//GEN-LAST:event_BtnUploadActionPerformed

