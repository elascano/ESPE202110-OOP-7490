    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        int renglon = tblShoes.getSelectedRow();
        if(renglon == -1){
            JOptionPane.showMessageDialog(this, "Error, Select the row to update");
            return;
        }
        String idUpload = tblShoes.getValueAt(renglon, 0).toString();
        int answer = JOptionPane.showConfirmDialog(this, "Are you sure to update the ID?"+ idUpload);
        if(answer == JOptionPane.OK_OPTION){
            Upload upload = new Upload(this,true, Workshop28, idUpload);
            upload.setVisible(true);
            view();
        }
    }//GEN-LAST:event_btnUploadActionPerformed

    
    public void add() {
        try {
            org.bson.Document data = new org.bson.Document();
            
            data.put("ID", Integer.parseInt(txtId.getText()));
            data.put("BRAND", txtBrand.getText());
            data.put("PRICE", txtPrice.getText());
            data.put("COLOR", txtColor.getText().toString());
            data.put("SIZE", txtSize.getText());

            Workshop28.insertOne(data);
            view();
            JOptionPane.showMessageDialog(this, "The data was added correctly");
           
        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "ERROR");
        }
    }
    
    public void view(){
        
        MongoCursor<Document> consult = Workshop28.find().iterator();
        
        int total = tabla.getRowCount();
        for(int i = 0; i<total; i++){
            tabla.removeRow(0);
        }
        while(consult.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(consult.next().values());
            tabla.addRow(doc.toArray());
        }

    }
    
    public boolean Delete(String id){
        DeleteResult answer = Workshop28.deleteOne(new Document("_id", new ObjectId(id)));
        if(answer.getDeletedCount()==1){
            return true;
        }
        return false;
    }
    
