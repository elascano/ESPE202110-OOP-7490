    public ArrayList<Person> returnPerson(){
        
        ArrayList<Person> person;
        person = new ArrayList<>();
        
        MongoCursor<Document> consulta = PersonalData.find().iterator();
        
        while(consulta.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(consulta.next().values());
            
            int idNumber = Integer.parseInt(doc.get(1).toString());
            String lastname = doc.get(2).toString();
            String name = doc.get(3).toString();
            
            
//            String day = Integer.toString(frmRegistration.calDateOfBirth.getCalendar().get(Calendar.DAY_OF_MONTH));
//            String month = Integer.toString(frmRegistration.calDateOfBirth.getCalendar().get(Calendar.MONTH));
//            String year = Integer.toString(frmRegistration.calDateOfBirth.getCalendar().get(Calendar.YEAR));
            
            //String dateOfBirth = (year + "/" + month + "/" + day);
            
            float salary = Float.parseFloat(doc.get(5).toString());  
            
            Person personData = new Person(idNumber, lastname, name, dateOfBirth, salary);        
            
            person.add(personData);
        }
        return person;
    }
    
