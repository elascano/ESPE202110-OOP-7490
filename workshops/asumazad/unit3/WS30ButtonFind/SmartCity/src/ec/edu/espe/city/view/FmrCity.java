    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int eli = JTableFlight.getSelectedRowCount();
        if (eli >= 0) {
            model.removeRow(eli);
            JOptionPane.showMessageDialog(this, "Successfull");
        } else {
            JOptionPane.showMessageDialog(null, "No information to delete");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void txtFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtFindActionPerformed
        String value = txtFind.getText();
        if (value.isEmpty()) {
            JTableFlight.clearSelection();
        } else {
            for (int i = 0; i < JTableFlight.getRowCount(); i++) {
                if (JTableFlight.getValueAt(i, 0).equals(value)) {
                    JTableFlight.requestFocus();
                    JTableFlight.changeSelection(i, 0, false, false);
                }
            }
        }
    }//GEN-LAST:event_txtFindActionPerformed

