    public void View(){
        
        MongoCursor<Document> consult = wilshiled.find().iterator();
        
        int total = table.getRowCount();
        for(int i = 0; i<total; i++){
            table.removeRow(0);
        }
        while(consult.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(consult.next().values());
            table.addRow(doc.toArray());
        }
    }
    public void Add() {
        try {
            org.bson.Document data = new org.bson.Document();
            
            data.put("ID Tire", Integer.parseInt(txtId.getText()));
            data.put("color", Integer.parseInt(txtId.getText()));
            data.put("armored windshield", Integer.parseInt(txtLoad.getText()));
            data.put("Brand", txtBrand.getText());
            data.put("Price", Integer.parseInt(txtId.getText()));

            wilshiled.insertOne(data);
            View();
            JOptionPane.showMessageDialog(this, "Added correctly");
           
        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "---> ERROR <---");
        }
    }
