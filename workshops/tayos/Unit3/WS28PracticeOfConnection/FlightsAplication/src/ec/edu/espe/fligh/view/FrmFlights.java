    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int renglon = tblFlights.getSelectedRow();
        if (renglon == -1) {
            JOptionPane.showMessageDialog(this, "Error, Select what you want to delete");
            return;
        }
        String idRemove = tblFlights.getValueAt(renglon, 0).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, "Are you sure you want to Delete this id?" + idRemove);
        if (respuesta == JOptionPane.OK_OPTION) {
            boolean answerDelete = Delete(idRemove);
            if (answerDelete == true) {
                mostrar();
                JOptionPane.showMessageDialog(this, "Deleted Correctly");
            } else {
                JOptionPane.showMessageDialog(this, "Could not be eliminated");

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

