    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        txtName.getText();
        txtOwner.getText();
        txtCoach.getText();
        txtColor.getText();
        txtStadium.getText();
       
       Object nba[] = new Object[7];
        nba [0]= txtName.getText();
        nba [1]= txtOwner.getText();
        nba [2]= txtCoach.getText();
        nba [3]= txtColor.getText();
        nba [4]= txtStadium.getText();
        tableNba.addRow(nba);
      
        try {
            Document data = new org.bson.Document();
            data.put("Name", txtName.getText());
            data.put("Owner",txtOwner.getText());
            data.put("Coach", txtCoach.getText());
            data.put("Color", txtColor.getText());
            data.put("Stadium", txtStadium.getText());
            
            Nba.insertOne(data);
            JOptionPane.showMessageDialog(this, "Data Added Successful ");

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this, "ERROR: " + err.getMessage());
        }       
                                        
    }//GEN-LAST:event_jButton1ActionPerformed

