    public void total() {
        int multiply, multiplier , product;
        multiply = Integer.parseInt(txtQuantity.getText());
        multiplier = Integer.parseInt(txtPrice.getText());
        
        product = multiply * multiplier;
        txtTotal .setText(String.valueOf(product));
    }
