    public void View(){
        MongoCursor<Document> consult = Tire.find().iterator();
        
        int total = table.getRowCount();
        for(int i = 0; i<total; i++){
            table.removeRow(0);
        }
        while(consult.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(consult.next().values());
            table.addRow(doc.toArray());
        }
    }
    
    public void Add() {
        try {
            org.bson.Document data = new org.bson.Document();
            
            data.put("ID Tire", Integer.parseInt(txtId.getText()));
            data.put("Type of Tire", txtType.getSelectedItem().toString());
            data.put("Radial", Integer.parseInt(txtRadial.getText()));
            data.put("Load Capacity", Integer.parseInt(txtLoad.getText()));
            data.put("Brand", txtBrand.getText());
            data.put("Price", String.valueOf(txtPrice.getText()));

            Tire.insertOne(data);
            View();
            JOptionPane.showMessageDialog(this, "The data was added correctly");
           
        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "ERROR");
        }
    }
     public ArrayList<Tire> returnTires(){
        
        ArrayList<Tire> tires;
        tires = new ArrayList<>();
        MongoCursor<Document> consulta = Tire.find().iterator();
        
        while(consulta.hasNext()){ 
            ArrayList<Object> doc = new ArrayList<Object>(consulta.next().values());
            
            int id = Integer.parseInt(doc.get(1).toString());
            String typeOftire = doc.get(2).toString();
            int radial = Integer.parseInt(doc.get(3).toString());
            int loadcapacity= Integer.parseInt(doc.get(4).toString());
            String brand = doc.get(5).toString();
            float price = Float.parseFloat(doc.get(6).toString());
            
            Tire tire = new Tire(id, typeOftire, radial, loadcapacity, brand, price);
            tires.add(tire);
        }
        return tires;
    }
    
    public Tire findTires(int id){
        ArrayList<Tire> tires;
        tires = new ArrayList<>();
        tires = returnTires();
        
        for(Tire tire:tires){
            if(tire.getId()==id){
                return tire;
            }
        }
        return null; 
    }

