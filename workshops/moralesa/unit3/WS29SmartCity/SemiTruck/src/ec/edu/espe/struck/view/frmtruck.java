        public void add() {
        try {
            Document data = new Document();
            
            data.put("id", Integer.parseInt(txtId.getText()));
            data.put("Brand", txtBrand.getSelectedItem().toString());
            data.put("Color", txtColor.getText());
            data.put("Styles", txtStyles.getSelectedItem().toString());
            data.put("Price", Float.parseFloat(txtPrice.getText()));
            
            Truck.insertOne(data);
            view();
            JOptionPane.showMessageDialog(this, "Success");
           
        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "ERROR: "+err.getMessage());
        }
    }
    public void view(){
        
        MongoCursor<Document> consult = Truck.find().iterator();
        
        int total = tabla.getRowCount();
        for(int i = 0; i<total; i++){
            tabla.removeRow(0);
        }
        while(consult.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(consult.next().values());
            tabla.addRow(doc.toArray());
        }

    }    
        
