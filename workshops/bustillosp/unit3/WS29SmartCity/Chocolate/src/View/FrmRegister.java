    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String user = txtUser.getText();
        String licensePlate = txtLicensePlate.getText();
        String color = (String) cmbColor.getSelectedItem();
        String type = (String) cmbType.getSelectedItem();
        String arrivalTime = txtArrivalTime.getText();
        String exitTime = txtExitTime.getText();
        


        Chocolate car = new Chocolate(user, licensePlate, color, arrivalTime, exitTime, type);
        parking.add(car);
        JOptionPane.showMessageDialog(this, "Successful register -->" + licensePlate);

        try {
            Document data = new org.bson.Document();
            data.put("ID", txtUser.getText());
            data.put("Brand", txtLicensePlate.getText());
            data.put("Flavuor", cmbColor.getSelectedItem());
            data.put("Type", cmbType.getSelectedItem());
            data.put("ID Supplier", txtArrivalTime.getText());
            data.put("Unit Price", txtExitTime.getText());
            ParkingGarage.insertOne(data);
            txtUser.setText("");
            txtLicensePlate.setText("");
            cmbColor.setSelectedItem("");
            cmbType.setSelectedItem("");
            txtArrivalTime.setText("");
            txtExitTime.setText("");

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this, "ERROR: " + err.getMessage());
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

