    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String licensePlate;
        Chocolate car;
        licensePlate = JOptionPane.showInputDialog("Please, enter the Brand");
        for (int i = 0; i < parking.size(); i++) {
            car = (Chocolate) parking.get(i);
            if (licensePlate.equalsIgnoreCase(car.getLicensePlate())) {
                txtUser.setText(car.getUser());
                txtLicensePlate.setText(car.getLicensePlate());
                cmbColor.setSelectedItem(car.getColor());
                cmbType.setSelectedItem(car.getType());
                txtArrivalTime.setText(car.getArrivalTime());
                txtExitTime.setText(car.getExitTime());
                find = 1;
                break;
            }
        }

    }//GEN-LAST:event_btnFindActionPerformed

