    private void BtnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUploadActionPerformed
        try {
            Document data = new org.bson.Document();
            data.put("Color", TxtColor.getText());
            data.put("Material", TxtMaterial.getText());
            data.put((String) "Model",CbmModel.getSelectedItem());
            data.put((String) "Brand",CbmBrand.getSelectedItem());
            data.put((String) "Size",CbmSize.getSelectedItem());
            data.put("Price", TxtPrice.getText());
            PantsList.insertOne(data);
            JOptionPane.showMessageDialog(this, "Successful");

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this, "ERROR: " + err.getMessage());
        }
    }//GEN-LAST:event_BtnUploadActionPerformed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        int eli = JTablePants.getSelectedRowCount();
        if (eli >= 0) {
            model.removeRow(eli);
            JOptionPane.showMessageDialog(this, "Successfull");
        } else {
            JOptionPane.showMessageDialog(null, "No information to delete");
        }
    }//GEN-LAST:event_BtnDeleteActionPerformed

