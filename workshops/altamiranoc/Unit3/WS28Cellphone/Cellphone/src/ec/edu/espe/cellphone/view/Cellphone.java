    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        int renglon = JTableCellphone.getSelectedRow();
        if(renglon == -1){
            JOptionPane.showMessageDialog(this, "Error, Select the user to delete");
            return;
        }
        String idRemove = JTableCellphone.getValueAt(renglon, 0).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, "Are you shure to delete?");
        if(respuesta == JOptionPane.OK_OPTION){
            boolean answerDelete = Dalete(idRemove);
            if(answerDelete==true){
                view();
                JOptionPane.showMessageDialog(this, "The user was successfully deleted");
            }else{
                JOptionPane.showMessageDialog(this, "The user could not been deleted");
            }
        }    
    }//GEN-LAST:event_BtnDeleteActionPerformed

