    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int renglon = tblDjMixer.getSelectedRow();
        if(renglon == -1){
            JOptionPane.showMessageDialog(this, "Selecciona el ID a borrar");
            return;
        }
        String idRemove = tblDjMixer.getValueAt(renglon, 0).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, "Â¿Estas seguro de eliminar este ID?"+ idRemove);
        if(respuesta == JOptionPane.OK_OPTION){
            boolean answerDelete = Delete(idRemove);
            if(answerDelete==true){
                mostrar();
                JOptionPane.showMessageDialog(this, "Correct");
            }else{
                JOptionPane.showMessageDialog(this, "Error");

            }
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed

        try {
            Document data = new Document();

            data.put("Name", txtName.getText());
            data.put("Id", txtId.getText());
            data.put("PhoneNumber", txtPhoneNumber.getText());
            data.put("Price", txtprice.getText());
            data.put("Specialty", txtSpecialty.getText());
            DjMixer.insertOne(data);

            JOptionPane.showMessageDialog(this, "CORRECT");

        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "ERROR: "+err.getMessage());
        }

    }//GEN-LAST:event_btnInsertActionPerformed

    public boolean Delete(String id){
        DeleteResult answer = DjMixer.deleteOne(new Document("_id", new ObjectId(id)));
        if(answer.getDeletedCount()==1){
            return true;
        }
        return false;
    }
