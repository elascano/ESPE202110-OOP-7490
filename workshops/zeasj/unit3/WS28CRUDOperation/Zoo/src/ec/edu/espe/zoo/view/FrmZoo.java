    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       
        int renglon = tblAnimalList.getSelectedRow();
        if(renglon == -1){
            JOptionPane.showMessageDialog(this, "Error, select the row to delete");
            return;
        }
        String idRemove = tblAnimalList.getValueAt(renglon, 0).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, "Are you sure to delete the id?  "+ idRemove);
        if(respuesta == JOptionPane.OK_OPTION){
            boolean answerDelete = Delete(idRemove);
            if(answerDelete==true){
                JOptionPane.showMessageDialog(this, "**Successfully Removed**");
            }else{
                JOptionPane.showMessageDialog(this, "Could not be removed");

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListActionPerformed
        
        MongoCursor<Document> animalList = Zoo.find().iterator();

        int total = table.getRowCount();
        for(int i = 0; i<total; i++){
            table.removeRow(0);
        }
        while(animalList.hasNext()){
            ArrayList<Object> animalDoc = new ArrayList<Object>(animalList.next().values());
            table.addRow(animalDoc.toArray());
        }
        
    }//GEN-LAST:event_btnListActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
                
        String sex;
        if(rbtMale.isSelected()){
            sex = "Male";
        }else if (rbtFemale.isSelected()){
            sex = "Female";
        }
        try {
            Document data = new Document();

            data.put("Id", Integer.parseInt(txtId.getText()));
            data.put("Name", txtName.getText());
            data.put("Type", cmbType.getSelectedItem().toString());
            data.put("Sex", rbtFemale.getSelectedIcon());
            data.put("Origin", txtOrigin.getText());
            data.put("Comment", txtComment.getText());

            Zoo.insertOne(data);

            JOptionPane.showMessageDialog(this, "**Animal Added**");

        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "Error: "+err.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

