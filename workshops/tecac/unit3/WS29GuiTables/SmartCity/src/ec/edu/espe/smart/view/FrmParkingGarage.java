    public void seeCars() {
        MongoCursor<Document> carsQuery = Cars.find().iterator();

        int total = tableCars.getRowCount();
        for (int i = 0; i < total; i++) {
            tableCars.removeRow(0);
        }
        while (carsQuery.hasNext()) {
            ArrayList<Object> cars = new ArrayList<Object>(carsQuery.next().values());
            tableCars.addRow(cars.toArray());
        }
    }
    

