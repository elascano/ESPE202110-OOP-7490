    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String education = null;
        if(rbtnPublic.isSelected()){
                education = "Public";
            }else if (rbtnPrivate.isSelected()){
                education = "Private";
            }
        try {
            Document data = new Document();
            
            data.put("Name", txtName.getText());
            data.put("Public/Private", education);
            data.put("Amount of Students", Float.valueOf(txtStudents.getText()));
            data.put("Amount of Teachers", Float.valueOf(txtTeachers.getText()));
            data.put("Class Schedeule", lstSchedule.getSelectedValuesList().toString());
            HighSchool.insertOne(data);
            
            JOptionPane.showMessageDialog(this, "**High School Added**");
           
        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "ERROR!: "+err.getMessage());
        }
    }//GEN-LAST:event_btnInsertActionPerformed

