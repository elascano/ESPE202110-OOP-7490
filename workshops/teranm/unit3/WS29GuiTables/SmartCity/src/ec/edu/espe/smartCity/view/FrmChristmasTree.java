    private void btnFieldsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFieldsActionPerformed
        MongoCursor<Document> contactQuery = ChristmasTree.find().iterator();

        while (contactQuery.hasNext()) {
            ArrayList<Object> contactDoc = new ArrayList<Object>(contactQuery.next().values());
            table.addRow(contactDoc.toArray());
        }
    }//GEN-LAST:event_btnFieldsActionPerformed

    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        MongoCursor<Document> query = ChristmasTree.find().iterator();

        int total = table.getRowCount();
        for (int i = 0; i < total; i++) {
            table.removeRow(0);
        }
        while (query.hasNext()) {
            ArrayList<Object> doc = new ArrayList<Object>(query.next().values());
            table.addRow(doc.toArray());
        }
    }//GEN-LAST:event_btnRefreshActionPerformed

