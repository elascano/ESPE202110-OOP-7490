    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        try {
            Document data = new Document();
            
            data.put("id", Integer.parseInt(txtId.getText()));
            data.put("ManufacturerName", txtManufacturerName.getText());
            data.put("EngineColor", txtEngineColor.getText());
            data.put("PowerInWatts", Integer.parseInt(txtPowerInWatts.getText()));
            data.put("TypeOfMotor", jComboBox1.getSelectedItem().toString());
            data.put("Temperature", jComboBox2.getSelectedItem().toString());
          
            Test.insertOne(data);
            
            JOptionPane.showMessageDialog(this, "Success");
           
        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "ERROR: "+err.getMessage());
        }
            
    }//GEN-LAST:event_btnAddActionPerformed

