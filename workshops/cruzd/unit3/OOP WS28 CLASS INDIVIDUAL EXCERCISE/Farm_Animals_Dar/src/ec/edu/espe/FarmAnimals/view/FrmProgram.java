    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        try {
            Document data = new Document();
            
            data.put("id", Integer.parseInt(txtSerial.getText()));
            data.put("name", txtMake.getText());
            data.put("color", txtModel.getText());
            data.put("age", Integer.parseInt(txtPrice.getText()));
            data.put("sex", jComboBox1.getSelectedItem().toString());
            data.put("animal", jComboBox2.getSelectedItem().toString());
          
            Farm.insertOne(data);
            
            JOptionPane.showMessageDialog(this, "Success");
           
        } catch(Exception err){
            JOptionPane.showMessageDialog(this, "ERROR: "+err.getMessage());
        }
            
    }//GEN-LAST:event_btnAddActionPerformed
